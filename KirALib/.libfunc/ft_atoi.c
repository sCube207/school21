int		ft_atoi(char *str)
{
	int		d;
	int		is_neg;

	is_neg = 1;
	d = 0;
	while (*str == ' ' || *str == '\n' || *str == '\t')
		str++;
	if (*str == '-')
	{
		is_neg = -1;
		str++;
	}
	while (*str >= '0' && *str <= '9')
	{
		d *= 10;
		d += (*str++) - '0';
		if (d == 2147483647)
			return (2147483647);
		if (d == 214748364 && *str == '8' && is_neg == -1)
			return (-2147483648);
	}
	return (is_neg * d);
}
