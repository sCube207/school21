char	*ft_strncat(char *dest, char *str, int n)
{
	char	*ret;

	ret = dest;
	while (*dest != '\0')
		dest++;
	while (*str != '\0' && n-- != 0)
		*dest++ = *str++;
	*dest = '\0';
	return (ret);
}
